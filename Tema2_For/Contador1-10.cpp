#include <iostream>
using namespace std;

int main()
{
    cout << "Numeros del 1 al 10" << endl;
    // Iniciar en 1 y parar hasta que sea menor igual que 10, aumentado en uno en uno cada ciclo
    for (int i = 1; i <= 10; i++)
    {
        // Imprimir 
        cout << i << endl;
    }

    return 0;
}